int	ft_sqrt(int nb)
{
	int	steps;
	int	i;

	i = 1;
	steps = 0;
	while (nb > 0)
	{
		nb = nb - i;
		i = i + 2;
		steps++;
	}
	if (nb == 0)
		return (steps);
	return (0);
}
